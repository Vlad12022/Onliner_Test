package org.example.test;

public class MathUtil {

    public int plus(int a, int b) {
        return a + b;
    }

    public int mul(int a, int b) {
        return a * b;
    }

    public int div(int a, int b) {
        return a / b;
    }

    public int mod(int a, int b) {
        return a % b;
    }

    public int min(int a, int b) {
        return Math.min(a, b);
    }

}
